    public ClienteRedes(String twitterUsername, String instagramUsername) {
        initComponents();
        
        String seguidoresTwi = ClienteREST.askServerFor("twitterFollowers",twitterUsername);
        String seguidoresIns = ClienteREST.askServerFor("instagramFollowers",instagramUsername);
        
        this.usuarioTwitter.setText(twitterUsername);
        this.seguidoresTwitter.setText(seguidoresTwi);
        this.seguidosTwitter.setText(ClienteREST.askServerFor("twitterFollowed",twitterUsername));
        this.privacidadTwitter.setText(ClienteREST.askServerFor("twitterPrivacy",twitterUsername));
        if (ClienteREST.askServerFor("twitterVerified",twitterUsername).equals("Verificado"))
            this.verificadoTwitter.setText("Si");
        else
            this.verificadoTwitter.setText("No");
        this.descripcionTwitter.setText(ClienteREST.askServerFor("twitterBiography",twitterUsername));   
        
        this.usuarioInstagram.setText(instagramUsername);
        this.seguidoresInstagram.setText(seguidoresIns);
        this.seguidosInstagram.setText(ClienteREST.askServerFor("instagramFollowed",instagramUsername));
        this.privacidadInstagram.setText(ClienteREST.askServerFor("instagramPrivacy",instagramUsername));
        if (ClienteREST.askServerFor("instagramVerified",instagramUsername).equals("Verificado"))
            this.verificadoInstagram.setText("Si");
        else
            this.verificadoInstagram.setText("No");
        this.descripcionInstagram.setText(ClienteREST.askServerFor("instagramBiography",instagramUsername)); 
        
        this.descripcionTwitter.setLineWrap(true);
        this.descripcionTwitter.setWrapStyleWord(true);
        this.descripcionInstagram.setLineWrap(true);
        this.descripcionInstagram.setWrapStyleWord(true);

        int total = Integer.parseInt(seguidoresTwi) + Integer.parseInt(seguidoresIns);
        double media = (total * 1.0) / 2.0;
        this.totalSeguidores.setText(Integer.toString(total));
        this.mediaSeguidores.setText(Double.toString(media));
        
        setDefaultCloseOperation(ClienteRedes.DISPOSE_ON_CLOSE);
        
    }

